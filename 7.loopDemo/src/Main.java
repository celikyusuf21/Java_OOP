public class Main {

    public static void main(String[] args) {

        // For
        for(int i=2;i<10;i+=2){
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti.");

        //While
        int i=1;
        while(i<10){
            i+=3;
            System.out.println(i);
        }
        System.out.println("While Döngüsü bitti.");


    }
}
